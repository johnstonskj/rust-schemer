pub fn delete_file(file_name: SchemeString) -> Result<(), Error> {
    let file = PathBuf::from(file_name.deref());
    Ok(fs::remove_file(file)?)
}

pub fn file_exists(file_name: SchemeString) -> Result<Boolean, Error> {
    let file = PathBuf::from(file_name.deref());
    Ok(Boolean::from(file.exists()))
}

